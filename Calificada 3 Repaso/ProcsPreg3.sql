IF OBJECT_ID('SP_LISTARBARCO') IS NOT NULL
DROP PROC SP_LISTARBARCO
GO

CREATE PROCEDURE SP_LISTARBARCO
AS
SELECT * FROM [dbo].[TB_BARCO] 
GO

EXEC SP_LISTARBARCO
GO



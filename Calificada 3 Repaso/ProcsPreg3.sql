IF OBJECT_ID('SP_ULTIMOBARCO') IS NOT NULL
BEGIN
DROP PROC SP_ULTIMOBARCO
END
GO
CREATE PROC SP_ULTIMOBARCO
AS
SELECT TOP 1 [COD_BAR] FROM [dbo].[TB_BARCO]
ORDER BY [COD_BAR] DESC
GO

EXEC SP_ULTIMOBARCO
GO